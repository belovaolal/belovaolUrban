# Создаётся функция main()
def main():
    # Создание переменной my_string и присваивание ей значения строки с произвольным текстом (функция input())
    my_string = input("Введите текст: ")

    # Вывод длины символов введённого текста
    print("Длина текста:", len(my_string))

    # Работа с методами строк
    # Вывод строки my_string в верхнем регистре
    print("Верхний регистр:", my_string.upper())

    # Вывод строки my_string в нижнем регистре
    print("Нижний регистр:", my_string.lower())

    # Удаление всех пробелов в строке my_string
    my_string_without_spaces = my_string.replace(" ", "")
    print("Строка без пробелов:", my_string_without_spaces)

    # Вывод первого символа строки my_string
    print("Первый символ:", my_string[0])

    # Вывод последнего символа строки my_string
    print("Последний символ:", my_string[-1])


if __name__ == "__main__":
    main()